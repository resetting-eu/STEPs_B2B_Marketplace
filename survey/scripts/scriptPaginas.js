function toggleVisibility1() {
    var companyDescription = document.getElementById("company_description");
    var workCharacterization = document.getElementById("work_characterization");

    // Verifica se o "Company Description" está visível
    if (companyDescription.style.display !== "none") {
        // Se estiver visível, torna invisível o "Company Description" e visível o "Work Characterization"
        companyDescription.style.display = "none";
        workCharacterization.style.display = "block";
    } else {
        // Se estiver invisível, torna visível o "Company Description" e invisível o "Work Characterization"
        companyDescription.style.display = "block";
        workCharacterization.style.display = "none";
    }
}
